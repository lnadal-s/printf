#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	struct s_list		*ptr;
	int					k;

	k = 0;
	ptr = lst;
	while (ptr)
	{
		ptr = ptr->next;
		k++;
	}
	return (k);
}
