int		ft_atoi(const char *src)
{
	int					k;
	unsigned int		res;
	int					s;

	k = 0;
	s = 1;
	res = 0;
	while (src[k] && (src[k] == ' ' || src[k] == '\n' || src[k] == '\r' ||
				src[k] == '\t' || src[k] == '\f' || src[k] == '\v'))
	{
		k++;
	}
	if (src[k] && (src[k] == '+' || src[k] == '-'))
	{
		if (src[k] == '-')
			s = -s;
		k++;
	}
	while (src[k] && (src[k] >= '0' && src[k] <= '9'))
	{
		res = res * 10 + src[k] - '0';
		k++;
	}
	return (res * s);
}
