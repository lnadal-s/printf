#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	k;
	char	*ptr;

	ptr = (char *)s;
	k = 0;
	while (k < n)
	{
		ptr[k] = c;
		k++;
	}
	return (s);
}
